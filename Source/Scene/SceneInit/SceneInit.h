	//=============================================================================
	void Start();

	//=============================================================================
	// シーンの実行時に繰り返し呼び出される更新処理関数
	//=============================================================================
	void Update();

	//=============================================================================
	// シーンの実行時に繰り返し呼び出される描画処理関数
	//=============================================================================
	void Draw();

	//=============================================================================
