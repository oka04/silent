#pragma once

#include <Windows.h>

class IntervalManage
{
public:

	//=============================================================================
	// コンストラクタ
	//=============================================================================
	IntervalManage();

	//=============================================================================
	// 計測間隔時間の設定
	// 引　数：計測間隔時間（ミリ秒）
	//=============================================================================
	void SetInterval(const DWORD interval);

	//=============================================================================
	// 設定した計測間隔時間に達したかどうか調べる
	// 戻り値：true 経過した、false 経過していない
	//=============================================================================
	bool GetTiming();

	//=============================================================================
	// 計測間隔時間のリセット
	//=============================================================================
	void ResetInterval();

private:

	//計測開始時間
	DWORD m_start;

	//現在時間
	DWORD m_now;

	//経過時間
	DWORD m_pass;

	//計測間隔時間
	DWORD m_interval;
};

























