#pragma once

#include <d3dx9.h>

#include <dsound.h>

#pragma comment ( lib, "dsound.lib" )

class DS
{
public:

	//=============================================================================
	// コンストラクタ
	//=============================================================================
	DS();

	//=============================================================================
	// デストラクタ
	//=============================================================================
	~DS();

	//=============================================================================
	// サウンドバッファを登録
	// 引　数：LPDIRECTSOUNDBUFFER セカンダリバッファのポインタ
	//=============================================================================
	void SetSoundBuffer(LPDIRECTSOUNDBUFFER pSoundBuffer);

	//=============================================================================
	// サウンドバッファを取得
	// 戻り値：LPDIRECTSOUNDBUFFER セカンダリバッファのポインタ
	//=============================================================================
	LPDIRECTSOUNDBUFFER GetSoundBuffer();

	//=============================================================================
	// 標準の周波数を設定
	//=============================================================================
	void SetStandardFrequency();

	//=============================================================================
	// 周波数の増減値を設定
	// 引　数：int 増減値
	//=============================================================================
	void SetFrequency(int value);

	//=============================================================================
	// 標準のパンを設定
	//=============================================================================
	void SetStandardPan();

	//=============================================================================
	// パンの増減値を設定
	// 引　数：int 増減値
	//=============================================================================
	void SetPan(int value);

	//=============================================================================
	// 標準のボリュームを設定
	//=============================================================================
	void SetStandardVolume();

	//=============================================================================
	// ボリュームの増減値を設定
	// 引　数：int 増減値
	//=============================================================================
	void SetVolume(int value);

private:

	//セカンダリバッファ
	LPDIRECTSOUNDBUFFER m_pSoundBuffer;

	//標準の周波数
	long m_standardFrequency;

	//現在の周波数
	long m_frequency;

	//現在のパン
	long m_pan;

	//現在のボリューム
	long m_volume;

	//コピー防止
	DS& operator=(const DS&);
	DS(const DS&);
};