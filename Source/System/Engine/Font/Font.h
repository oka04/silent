#pragma once

#include <d3dx9.h>

class Font
{
public:

	//=============================================================================
	// コンストラクタ
	//=============================================================================
	Font();

	//=============================================================================
	// デストラクタ
	//=============================================================================
	~Font();

	//=============================================================================
	// フォントのポインタ(LPD3DXFONT)をクラスに登録
	// 引　数：LPD3DXFONT フォントのアドレス
	//=============================================================================
	void SetFontPointer(const LPD3DXFONT pFont);

	//=============================================================================
	// 登録したフォントのアドレスを取得
	// 戻り値：LPD3DXFONT
	//=============================================================================
	LPD3DXFONT GetFontPointer() const;

	//=============================================================================
	// フォントサイズの設定
	// 引　数：int フォントサイズ
	//=============================================================================
	void SetFontSize(const int nFontSize);

	//=============================================================================
	// フォントサイズの取得
	// 戻り値：int フォントサイズ
	//=============================================================================
	int GetFontSize() const;

private:

	//ID3DXFontのポインタ
	LPD3DXFONT m_pFont;

	//フォントサイズ
	int m_nFontSize;

	//コピー防止
	Font& operator=(const Font&);
	Font(const Font&);
};