#define _USING_V110_SDK71_ 1

#include "Font.h"

#include <cassert>

//=============================================================================
// コンストラクタ
//=============================================================================
Font::Font() 
	: m_pFont(nullptr)
	, m_nFontSize(0)
{

}

//=============================================================================
// デストラクタ
//=============================================================================
Font::~Font()
{
	if (m_pFont) {
		m_pFont->Release();
		m_pFont = nullptr;
	}
}

//=============================================================================
// フォントのポインタ(LPD3DXFONT)をクラスに登録
// 引　数：LPD3DXFONT フォントのアドレス
//=============================================================================
void Font::SetFontPointer(const LPD3DXFONT pFont)
{
	m_pFont = pFont;
}

//=============================================================================
// 登録したフォントのアドレスを取得
// 戻り値：LPD3DXFONT
//=============================================================================
LPD3DXFONT Font::GetFontPointer() const
{
	return m_pFont;
}

//=============================================================================
// フォントサイズの設定
// 引　数：int フォントサイズ
//=============================================================================
void Font::SetFontSize(const int nFontSize)
{
	assert(nFontSize > 0);

	m_nFontSize = nFontSize;
}

//=============================================================================
// フォントサイズの取得
// 戻り値：int フォントサイズ
//=============================================================================
int Font::GetFontSize() const
{
	return m_nFontSize;
}



